    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try 
        {
            if(new EquipoCtrl().guar(txtNombE.getText(),txtDesc.getText()))
            {
                JOptionPane.showMessageDialog(this,"Equipo guardado correctamente","SUCCES",JOptionPane.INFORMATION_MESSAGE);
                llenarTabla();
                limpiarE();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Error al guardar equipo, favor verifique","ERROR",JOptionPane.ERROR_MESSAGE);
            }
        } 
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(this,"Error al Procesar","ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

