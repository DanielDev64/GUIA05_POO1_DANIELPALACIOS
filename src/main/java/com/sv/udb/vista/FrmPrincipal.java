    private void llenarTabla()
    {
        try
        {
            DefaultTableModel model = (DefaultTableModel)this.tblEquipos.getModel();
            while(model.getRowCount()>0){model.removeRow(0);} //Limpiar modelo
            for(Equipos temp : new EquipoCtrl().consTodo())
            {
                model.addRow(new Object[]{temp.getCod(),temp, temp.getDesc()});
            }
            
            DefaultComboBoxModel<Equipos> mcmb = new DefaultComboBoxModel<>();
            while(mcmb.getSize()>0){mcmb.removeAllElements();} //Limpiar modelo
            for(Equipos temp : new EquipoCtrl().consTodo())
            {
                mcmb.addElement(temp);
            }
            this.cmbEquipo.setModel((DefaultComboBoxModel)mcmb);
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    
    private void TablaJuga()
    {
        try
        {
            DefaultTableModel model = (DefaultTableModel)this.tblJugadores.getModel();
            while(model.getRowCount()>0){model.removeRow(0);} //Limpiar modelo
            for(Jugadores temp : new JugadorCtrl().consTodo())
            {
                model.addRow(new Object[]{temp, temp.getCodiEqui(), temp.getEdadJuga(), temp.getAltuJuga(), temp.getPesoJuga()});
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    
    
