    private void tblJugadoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblJugadoresMouseClicked
        int fila = this.tblJugadores.getSelectedRow();
        if(fila >= 0)
        {
            Jugadores obje = (Jugadores)this.tblJugadores.getValueAt(fila, 0);
            Equipos equi = (Equipos)obje.getCodiEqui();
            this.codiJuga = obje.getCodiJuga();
       
            this.txtJugador.setText(obje.getNombJuga());
            this.txtEdad.setText(String.valueOf(obje.getEdadJuga()));
            this.txtAltura.setText(String.valueOf(obje.getAltuJuga()));
            this.txtPeso.setText(String.valueOf(obje.getPesoJuga()));
            
            DefaultComboBoxModel<Equipos> mcmb = (DefaultComboBoxModel)this.cmbEquipo.getModel();
            mcmb.setSelectedItem(equi);
            
            
        }
    }//GEN-LAST:event_tblJugadoresMouseClicked

    private void btnGuardarJActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarJActionPerformed
        try 
        {
            String jug = txtJugador.getText();
            int eda = Integer.parseInt(txtEdad.getText());
            int pes = Integer.parseInt(txtPeso.getText());
            int alt = Integer.parseInt(txtAltura.getText());
            Equipos objEqui = (Equipos)this.cmbEquipo.getSelectedItem();
            if(new JugadorCtrl().guar(objEqui,jug,eda,alt,pes))
            {
                JOptionPane.showMessageDialog(this,"Jugador guardado correctamente","SUCCES",JOptionPane.INFORMATION_MESSAGE);
                TablaJuga();
                limpiarJ();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Error al guardar jugador, favor verifique","ERROR",JOptionPane.ERROR_MESSAGE);
            }
        } 
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(this,"Error al Procesar","ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarJActionPerformed

    private void btnActualizarJActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarJActionPerformed
        try {
            if(codiJuga != 0)
            {
                String jug = txtJugador.getText();
                int eda = Integer.parseInt(txtEdad.getText());
                int pes = Integer.parseInt(txtPeso.getText());
                int alt = Integer.parseInt(txtAltura.getText());
                Equipos objEqui = (Equipos)this.cmbEquipo.getSelectedItem();
                if(new JugadorCtrl().actu(codiJuga,objEqui,jug,eda,alt,pes))
                {
                    JOptionPane.showMessageDialog(this,"Jugador actualizado correctamente","SUCCES",JOptionPane.INFORMATION_MESSAGE);
                    TablaJuga();
                    limpiarJ();
                }
                else{
                   JOptionPane.showMessageDialog(this,"Error al actualizar jugador, favor verifique","ERROR",JOptionPane.ERROR_MESSAGE);
                }
            }
            else{
                JOptionPane.showMessageDialog(this,"Debe seleccionar un registro, favor verifique","ERROR",JOptionPane.ERROR_MESSAGE);
            }      
        } 
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(this,"Error al Procesar","ERROR",JOptionPane.ERROR_MESSAGE);
            System.err.println(e.getMessage());
        }
    }//GEN-LAST:event_btnActualizarJActionPerformed

    private void btnEliminarJActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarJActionPerformed
        try {
            if(codiJuga != 0)
            {
                if (JOptionPane.showConfirmDialog(this, "Estas seguro de eliminar este jugador?", "WARNING",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                if(new JugadorCtrl().elim(codiJuga))
                {
                    JOptionPane.showMessageDialog(this,"Jugador eliminado correctamente","SUCCES",JOptionPane.INFORMATION_MESSAGE);
                    TablaJuga();
                    limpiarJ();
                }
                else{
                   JOptionPane.showMessageDialog(this,"Error al eliminar jugador, favor verifique","ERROR",JOptionPane.ERROR_MESSAGE);
                }
            }
            }
            else{
                JOptionPane.showMessageDialog(this,"Debe seleccionar un registro, favor verifique","ERROR",JOptionPane.ERROR_MESSAGE);
            }
        } 
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(this,"Error al Procesar","ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarJActionPerformed

